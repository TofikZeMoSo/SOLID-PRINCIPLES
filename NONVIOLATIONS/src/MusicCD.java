public class MusicCD {
    String CDName;
    int publishYear;
    int CDPrice;
    String singerName;

    public MusicCD(String CDName, int publishYear, int CDPrice, String singerName)
    {
        this.CDName = CDName;
        this.publishYear = publishYear;
        this.CDPrice = CDPrice;
        this.singerName=singerName;
    }
}
